<h3>Definisi Cloud Computing</h3>
definisi cloud computing (komputasi awan) merupakan gabungan pemanfaatan 
teknologi komputer (komputasi) dalam suatu jaringan dengan pengembangan
berbasis internet (awan) yang mempunyai fungsi untuk menjalankan program
atau aplikasi melalui komputer - komputer yang terkoneksi pada waktu 
yang sama, tetapi tak semua yang terkonekasi melalui internet 
menggunakan cloud computing.<br>
<h5>Cara Kerja Sistem Cloud Computing</h5>
Sistem Cloud bekerja menggunakan internet sebagai server dalam mengolah data.
Infrastruktur seperti media penyimpanan data dan juga instruksi/perintah dari pengguna 
disimpan secara virtual melalui jaringan internet kemudian perintah - perintah tersebut 
dilanjutkan ke server aplikasi. Setelah perintah diterima di server aplikasi kemudian 
data diproses dan pada proses final pengguna akan disajikan dengan halaman yang 
telah diperbaharui sesuai dengan instruksi yang diterima sebelumnya sehingga 
konsumen dapat merasakan manfaatnya.<br>
<h5>Kelebihan Cloud Computing:</h5>
1. Menghemat biaya dan ruang infratructure pembelian sumber daya komputer<br>
2. Bisa mengakses file dimana saja dan kapan saja<br>
3. Bisa menghemat waktu pada perusahaan sehingga bisa langsung 
   fokus pada perkembangan infrastructure<br>
4. Dapat dengan mudah di monitoring dari satu server<br>
5. Operasional dan manajemen lebih mudah dan sederhana<br>
6. Menghemat biaya operasional pada sistem informasi yang dibangun<br>
7. Kolaborasi yang terpercaya<br>
<h5>Kekurangan Cloud Computing:</h5>
1. Komputer akan menjadi lemot  atau lambat atau tidak bisa dipakai sama sekali 
    bila internet putus<br>
2. Komputer akan menjadi lambat kinerjanya jika koneksi internet kita juga lambat<br>
3. Komputer akan menjadi sangat lambat karena diakses oleh banyak pengguna sehingga 
   server akan menerima banyak sekali permintaan<br>
4. Jika tidak mempunyai backup yang handal maka hal terburuk ini akan timbul
   karena semua data berada di satu server pada cloud computing<br>
<h5>Karakteristik Cloud Computing:</h5>
1. On-Demand Self-Services
   Merupakan sebuah layanan cloud computing yang harus dapat dimanfaatkan 
   oleh para pengguna melalui beberapa mekanisme swalayan dan dapat langsung 
   tersedia pada saat dibutuhkan oleh para pengguna.<br>

2. Broad Network Access
   Merupakan sebuah layanan cloud computing harus bisa diakses dari kapan saja, 
   dimana saja, dengan software apa pun, yang terpenting kita terhubung 
   ke jaringan internet. misal : Handphone, tablet.<br>

3. Resource Pooling
   Merupakan sebuah layanan cloud computing yang harus tersedia secara terpusat 
   dan bisa membagi sumber daya secara cepat dan efisien.<br>

4. Rapid Elasticity
   Merupakan sebuah layanan cloud computing yang harus dapat menaikan dan menurunkan 
   kapasitas sesuai dengan kebutuhan server dan pengguna.<br>

5. Measured Service
   Merupakan sebuah layanan cloud computing yang harus disediakan secara terukur 
   dan teratur, karena ini akan dilakukan dalam proses pembayaran.<br>
