<h5>Apache Hadoop</h5>
Apache Hadoop adalah software bebas dan open source, 
yang ditulis dalam kerangka bahasa pemrograman Java 
untuk dijalankan secara terdistribusi dan skalabel. 
Ia dibangun berdasarkan algoritma popular MapReduce 
dari Google Inc, berikut sistem berkas yang disarankan 
Google (GFS = Google File System), yang memungkinkan 
menjalankan tugas komputasi intensif dalam mengolah 
data jumlah besar (Big Data, sekitar petabyte keatas) 
di komputer cluster dengan hardware komoditas.
