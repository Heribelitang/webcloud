<h3>Berikut pengertian Iaas, Paas, Saas, dan Xaas</h3>
<p>Infrastructure as a Service (IaaS)<br>
Infrastructure as a Service adalah layanan komputasi awan yang menyediakan infrastruktur IT berupa CPU, RAM, storage, bandwith dan konfigurasi lain.
Keuntungan layanan IaaS ini adalah tidak perlu membeli komputer fisik sehingga lebih menghemat biaya. Konfigurasi komputer virtual juga bisa diubah
sesuai kebutuhan. Misalkan saat storage hampir penuh, storage bisa ditambah dengan segera. Perusahaan yang menyediakan IaaS adalah Amazon EC2, 
TelkomCloud dan BizNetCloud.<br>
<h5>Platform as a Service (PaaS)</h5>
Platform as a Service adalah layanan yang menyediakan computing platform. Biasanya sudah terdapat sistem operasi, database, web server dan framework 
aplikasi agar dapat menjalankan aplikasi yang telah dibuat.Keuntungan layanan PaaS ini bagi pengembang adalah mereka bisa fokus pada aplikasi yang 
mereka buat tanpa memikirkan tentang pemeliharaan dari computing platform. Contoh penyedia layanan PaaS adalah Amazon Web Service dan Windows Azure.
<br><h5>Software as a Service (SaaS)</h5>
Software as a Service adalah layanan komputasi awan dimana kita bisa langsung menggunakan aplikasi yang telah disediakan. Penyedia layanan mengelola 
infrastruktur dan platform yang menjalankan aplikasi tersebut. Contoh layanan aplikasi email yaitu gmail, yahoo dan outlook sedangkan contoh aplikasi 
media sosial adalah twitter, facebook dan google+. Keuntungan dari layanan ini adalah pengguna tidak perlu membeli lisensi untuk mengakses aplikasi tersebut. 
<br>Ada beberapa tipe software yang ditawarkan pada SaaS, yaitu :
1.Manajemen resources bagi pelanggan<br>
2.Video Conferencing<br>
3.Manajemen Layanan IT<br>
4.Akunting<br>
5.Analisa Web<br>
6.Manajemen web content<br>
<br><h5>Everything as a Service (XaaS)</h5>
XaaS adalah istilah kolektif mengatakan untuk berdiri untuk sejumlah hal termasuk " X sebagai layanan ,"apa pun sebagai layanan" atau "segala sesuatu sebagai layanan". 
akronim mengacu peningkatan jumlah layanan yang disampaikan melalui Internet daripada yang tersedia secara lokal atau di tempat . XaaS adalah esensi dari komputasi awan 
